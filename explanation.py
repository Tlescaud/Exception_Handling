# try: The block of code that might raise an exception.
#
# except: The block of code that will run if an exception occurs.
#
# else: The block of code that will run if no exception occurs in the try block.
#
# finally: The block of code that will always run, regardless of whether an exception occurred.


# summary try...except: Handles exceptions gracefully without stopping the program.
#
# try...except...finally: Ensures a block of code runs whether an exception occurs or not.
#
# try...except...else: Adds an else block to run code only if no exception occurs.